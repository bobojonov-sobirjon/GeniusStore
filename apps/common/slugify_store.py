"""Slug generation aligned with NestJS `generateSlug` (helpers.ts)."""
import re
import unicodedata

_CYRILLIC_TO_LATIN = {
    'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo', 'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y',
    'к': 'k', 'л': 'l', 'м': 'm', 'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u', 'ф': 'f',
    'х': 'kh', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'shch', 'ъ': '', 'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya',
    'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D', 'Е': 'E', 'Ё': 'Yo', 'Ж': 'Zh', 'З': 'Z', 'И': 'I', 'Й': 'Y',
    'К': 'K', 'Л': 'L', 'М': 'M', 'Н': 'N', 'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T', 'У': 'U', 'Ф': 'F',
    'Х': 'Kh', 'Ц': 'Ts', 'Ч': 'Ch', 'Ш': 'Sh', 'Щ': 'Shch', 'Ъ': '', 'Ы': 'Y', 'Ь': '', 'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya',
}


def translate_to_latin(text: str) -> str:
    return ''.join(_CYRILLIC_TO_LATIN.get(c, c) for c in text)


def generate_slug(text: str) -> str:
    translated = translate_to_latin(text)
    normalized = unicodedata.normalize('NFD', translated)
    without_marks = ''.join(c for c in normalized if unicodedata.category(c) != 'Mn')
    lower = without_marks.lower().strip()
    lower = re.sub(r'[^a-z0-9\s-]', '', lower)
    lower = re.sub(r'[\s-]+', '-', lower)
    return re.sub(r'^-+|-+$', '', lower)
